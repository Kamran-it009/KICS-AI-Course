a = 10
b = 23
print(a + b) # addition
print(a - b) # Subtraction
print(a * b) # Multiplication
print(a / b) # Division
print(a % b) # Remainder Operator(Modulus)
print(a // b) # Integer Division(Gives answer in Integer)

# Logical Operators
# and
# or
# not

# And Operator
print(a > 5 and b > 20) # (True) if both are true.
print(a > 5 and b < 20) # (False) if anyone is false.

# OR Operator
print(a > 5 or b < 20) # (True) if anyone is true.
print(a < 5 or b < 20) # (False) if both are true.

# Not Operator
print(not b < 20) # Reverse the result.


# Concatenation Operator
name1 = 'Kamran'
name2 = 'Khan'
print(name1 + name2)

# Exponent Operator
x = 2
print(x**3)
